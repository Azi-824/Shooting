//################# 列挙型 #################
enum BT_TYPE
{
	BT_START,		//スタート
	BT_END			//エンド
};

//################# クラス定義 ################
class Button
{
private:

	Image* img;	//画像
	RECT rect;	//領域

	int Element;			//要素番号
	static int ElementCnt;	//要素数

public:

	Button(Image*);	//コンストラクタ
	~Button();		//デストラクタ

	void SetInit(int,int);	//初期設定

	bool OnClick();	//クリックされたか
	void Draw();	//描画
	int GetElement();	//要素番号取得
	
	template <typename F>
	void Event(F func)	//クリックされた時のイベント
	{
		func();
	}

};
